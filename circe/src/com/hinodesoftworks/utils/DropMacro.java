package com.hinodesoftworks.utils;

import android.graphics.drawable.Drawable;

public class DropMacro
{
	private String macroLabel = "N/A";
	private Drawable macroImage;
	
	public String getMacroLabel()
	{
		return macroLabel;
	}
	public void setMacroLabel(String macroLabel)
	{
		this.macroLabel = macroLabel;
	}
	
	public Drawable getMacroImage()
	{
		return macroImage;
	}
	public void setMacroImage(Drawable macroImage)
	{
		this.macroImage = macroImage;
	}
	
	
}
